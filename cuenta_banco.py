class Cuenta():

    def estado (self):
        estado = 0
        return estado

    def sumar(num1, num2):
        return num1 + num2

    def restar(estado, num1):
        return estado - num1

    def intereses(estado, num1):
        return estado * num1

    def crecimiento(estado, intereses):
        nuevo_estado = intereses + estado
        return nuevo_estado
